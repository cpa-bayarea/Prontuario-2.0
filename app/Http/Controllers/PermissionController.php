    public function updatePermissions()
    {
        try {
            foreach (Route::getRoutes() as $route) {
                if ($route->getName()) {
                    Permission::firstOrCreate([
                        'nome' => $route->getName()
                    ]);
                }
            }
            return ["success" => true];
        } catch (Exception  $e) {
            return ["error" => $e->getMessage()];
        }
    }

